def _claude_worker_model(env: Mapping[str, str]) -> str:
    raw = _clean(
        env.get("CHIMERA_MEMORY_CLAUDE_WORKER_MODEL")
        or env.get("CHIMERA_MEMORY_CLI_WORKER_MODEL"),
        max_chars=120,
    )
    default = _default_claude_memory_worker_model()
    allow_opus = _env_bool(env, "CHIMERA_MEMORY_CLAUDE_WORKER_ALLOW_OPUS", default=False)
    if not raw:
        return default
    if "opus" in raw.lower() and not allow_opus:
        return default
    return raw


def _claude_worker_effort(env: Mapping[str, str]) -> str:
    effort = _env_choice(
        env,
        ("CHIMERA_MEMORY_CLAUDE_WORKER_EFFORT", "CHIMERA_MEMORY_CLI_WORKER_EFFORT"),
        default="medium",
        allowed={"low", "medium", "high", "xhigh", "max"},
    )
    allow_opus = _env_bool(env, "CHIMERA_MEMORY_CLAUDE_WORKER_ALLOW_OPUS", default=False)
    if not allow_opus and effort in {"xhigh", "max"}:
        return "medium"
    return effort


