def _extract_cli_stdout_metrics(path: Path) -> dict[str, Any]:
    metrics: dict[str, Any] = {
        "input_tokens": 0,
        "output_tokens": 0,
        "cache_creation_input_tokens": 0,
        "cache_read_input_tokens": 0,
        "model": "",
        "session_id": "",
    }

    def visit(value: object) -> None:
        if isinstance(value, dict):
            for key, target in (
                ("input_tokens", "input_tokens"),
                ("output_tokens", "output_tokens"),
                ("cache_creation_input_tokens", "cache_creation_input_tokens"),
                ("cache_read_input_tokens", "cache_read_input_tokens"),
            ):
                raw = value.get(key)
                if isinstance(raw, int):
                    metrics[target] = max(int(metrics[target]), raw)
            if not metrics["session_id"]:
                session_id = str(value.get("session_id") or value.get("sessionId") or "").strip()
                if session_id:
                    metrics["session_id"] = session_id[:120]
            model = str(value.get("model") or "").strip()
            if model:
                metrics["model"] = model[:120]
            for child in value.values():
                visit(child)
        elif isinstance(value, list):
            for child in value:
                visit(child)

    try:
        with path.open("r", encoding="utf-8", errors="ignore") as handle:
            for line in handle:
                if not line.strip() or not any(marker in line for marker in ("usage", "session", "model")):
                    continue
                try:
                    visit(json.loads(line))
                except json.JSONDecodeError:
                    continue
    except OSError:
        pass
    return metrics


def _worker_job_counts(conn: sqlite3.Connection, *, worker_id: str, since_iso: str) -> dict[str, int]:
    counts = {"claimed": 0, "succeeded": 0, "failed": 0}
    try:
        claimed = conn.execute(
            """
            SELECT COUNT(*)
            FROM memory_audit_events
            WHERE event_type = 'memory_worker_job_claimed'
              AND json_extract(payload, '$.worker_id') = ?
              AND julianday(created_at) >= julianday(?)
            """,
            (worker_id, since_iso),
        ).fetchone()
        result_rows = conn.execute(
            """
            SELECT COALESCE(json_extract(payload, '$.status'), ''), COUNT(*)
            FROM memory_audit_events
            WHERE event_type = 'memory_worker_result_submitted'
              AND json_extract(payload, '$.worker_id') = ?
              AND julianday(created_at) >= julianday(?)
            GROUP BY COALESCE(json_extract(payload, '$.status'), '')
            """,
            (worker_id, since_iso),
        ).fetchall()
    except sqlite3.Error:
        return counts
    counts["claimed"] = int((claimed or [0])[0] or 0)
    for status, count in result_rows:
        if status == "succeeded":
            counts["succeeded"] += int(count or 0)
        elif status == "failed":
            counts["failed"] += int(count or 0)
    if counts["claimed"] == 0:
        try:
            rows = conn.execute(
                """
                SELECT status, COUNT(*)
                FROM memory_enhancement_jobs
                WHERE locked_by_worker = ? AND julianday(updated_at) >= julianday(?)
                GROUP BY status
                """,
                (worker_id, since_iso),
            ).fetchall()
        except sqlite3.Error:
            rows = []
        for status, count in rows:
            counts["claimed"] += int(count or 0)
            if status == "succeeded":
                counts["succeeded"] += int(count or 0)
            elif status == "failed":
                counts["failed"] += int(count or 0)
    if counts["claimed"] == 0:
        counts["claimed"] = counts["succeeded"] + counts["failed"]
    return counts


def _record_cli_worker_pass(
    config: CodexCliWorkerConfig | ClaudeCliWorkerConfig | AgyCliWorkerConfig,
    handle: CodexCliWorkerHandle,
    *,
    runtime: str,
    returncode: int | None,
) -> None:
    metrics = _extract_cli_stdout_metrics(handle.stdout_log)
    latency_ms = max(0, int((time.monotonic() - handle.started_at_monotonic) * 1000)) if handle.started_at_monotonic else 0
    status = "succeeded" if returncode in (0, None) else "failed"
    session_id = handle.session_id or str(metrics.get("session_id") or "")
    try:
        with sqlite3.connect(config.db_path) as conn:
            jobs = _worker_job_counts(conn, worker_id=config.worker_id, since_iso=handle.started_at_iso)
            conn.execute(
                """
                INSERT INTO memory_cli_worker_pass_events (
                    worker_id, provider, runtime, session_mode, session_id, session_day,
                    resumed, status, returncode, stdout_log, stderr_log,
                    jobs_claimed, jobs_succeeded, jobs_failed,
                    tokens_in, tokens_out, cache_creation_input_tokens,
                    cache_read_input_tokens, latency_ms, model, metadata
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                """,
                (
                    config.worker_id,
                    config.provider,
                    runtime,
                    handle.session_mode,
                    session_id,
                    handle.session_day,
                    1 if handle.resumed_session else 0,
                    status,
                    int(returncode or 0),
                    str(handle.stdout_log),
                    str(handle.stderr_log),
                    jobs["claimed"],
                    jobs["succeeded"],
                    jobs["failed"],
                    int(metrics.get("input_tokens") or 0),
                    int(metrics.get("output_tokens") or 0),
                    int(metrics.get("cache_creation_input_tokens") or 0),
                    int(metrics.get("cache_read_input_tokens") or 0),
                    latency_ms,
                    str(metrics.get("model") or "")[:120],
                    json.dumps({"stdout_bytes": handle.stdout_log.stat().st_size if handle.stdout_log.exists() else 0}),
                ),
            )
            conn.commit()
    except sqlite3.Error:
        logging.getLogger("chimera_memory.cli-worker").exception(
            "failed to record cli worker pass worker_id=%s runtime=%s",
            config.worker_id,
            runtime,
        )

    if status == "succeeded" and handle.session_mode == "daily" and handle.session_metadata_path:
        turns = int((_read_daily_session(
            handle.session_metadata_path,
            runtime=runtime,
            worker_id=config.worker_id,
            provider=config.provider,
            day=handle.session_day,
        ) or {}).get("turns") or 0) + 1
        created_at = str((_read_daily_session(
            handle.session_metadata_path,
            runtime=runtime,
            worker_id=config.worker_id,
            provider=config.provider,
            day=handle.session_day,
        ) or {}).get("created_at") or _utc_now_iso())
        _write_daily_session_state(
            handle.session_metadata_path,
            runtime=runtime,
            worker_id=config.worker_id,
            provider=config.provider,
            session_day=handle.session_day,
            session_id=session_id,
            turns=turns,
            created_at=created_at,
        )


def cli_worker_stats(
    conn: sqlite3.Connection,
    *,
    hours: int = 24,
    limit: int = 12,
    env: Mapping[str, str] | None = None,
) -> dict[str, Any]:
    """Return token and performance telemetry for CLI worker passes."""
    hours = max(1, min(24 * 30, int(hours or 24)))
    limit = max(1, min(100, int(limit or 12)))
    source = env or os.environ
    cutoff_expr = f"strftime('%Y-%m-%dT%H:%M:%fZ', 'now', '-{hours} hours')"
    summary_rows = conn.execute(
        f"""
        SELECT runtime, provider, worker_id,
               COUNT(*) AS passes,
               SUM(CASE WHEN resumed THEN 1 ELSE 0 END) AS resumed_passes,
               SUM(jobs_claimed) AS jobs_claimed,
               SUM(jobs_succeeded) AS jobs_succeeded,
               SUM(jobs_failed) AS jobs_failed,
               SUM(tokens_in) AS tokens_in,
               SUM(tokens_out) AS tokens_out,
               SUM(cache_creation_input_tokens) AS cache_creation_input_tokens,
               SUM(cache_read_input_tokens) AS cache_read_input_tokens,
               AVG(latency_ms) AS avg_latency_ms,
               MAX(latency_ms) AS max_latency_ms
        FROM memory_cli_worker_pass_events
        WHERE created_at >= {cutoff_expr}
        GROUP BY runtime, provider, worker_id
        ORDER BY passes DESC
        """
    ).fetchall()
    recent_rows = conn.execute(
        f"""
        SELECT created_at, runtime, provider, worker_id, session_mode, session_id,
               resumed, status, returncode, jobs_claimed, jobs_succeeded, jobs_failed,
               tokens_in, tokens_out, cache_creation_input_tokens,
               cache_read_input_tokens, latency_ms, model
        FROM memory_cli_worker_pass_events
        WHERE created_at >= {cutoff_expr}
        ORDER BY created_at DESC
        LIMIT ?
        """,
        (limit,),
    ).fetchall()
    pending_rows = conn.execute(
        """
        SELECT requested_provider, COUNT(*)
        FROM memory_enhancement_jobs
        WHERE status = 'pending'
        GROUP BY requested_provider
        ORDER BY COUNT(*) DESC
        """
    ).fetchall()
    pending_jobs = [{"provider": row[0], "count": int(row[1] or 0)} for row in pending_rows]
    pending_total = sum(item["count"] for item in pending_jobs)
    worker_enabled = _env_bool(source, "CHIMERA_MEMORY_ENHANCEMENT_WORKER", default=True)
    auto_enqueue = _env_bool(source, "CHIMERA_MEMORY_ENHANCEMENT_AUTO_ENQUEUE", default=False)
    worker_mode = _clean(
        source.get("CHIMERA_MEMORY_ENHANCEMENT_WORKER_MODE"),
        default="dry_run",
        max_chars=80,
    ).lower().replace("-", "_")
    runtime = _clean(source.get("CHIMERA_MEMORY_CLI_WORKER_RUNTIME"), max_chars=80)
    worker_id = _clean(
        source.get(f"CHIMERA_MEMORY_{runtime.upper()}_WORKER_ID") if runtime else "",
        default=f"{runtime}-memory-worker-1" if runtime else "",
        max_chars=120,
    )
    recent_activity_rows = []
    if worker_id:
        try:
            recent_activity_rows = conn.execute(
                f"""
                SELECT event_type, MAX(created_at), COUNT(*)
                FROM memory_audit_events
                WHERE event_type IN ('memory_worker_job_claimed', 'memory_worker_result_submitted')
                  AND json_extract(payload, '$.worker_id') = ?
                  AND created_at >= {cutoff_expr}
                GROUP BY event_type
                """,
                (worker_id,),
            ).fetchall()
        except sqlite3.Error:
            recent_activity_rows = []
    recent_activity = {
        "worker_id": worker_id,
        "claims": 0,
        "submissions": 0,
        "last_claimed_at": "",
        "last_submitted_at": "",
    }
    for event_type, timestamp, count in recent_activity_rows:
        if event_type == "memory_worker_job_claimed":
            recent_activity["claims"] = int(count or 0)
            recent_activity["last_claimed_at"] = str(timestamp or "")
        elif event_type == "memory_worker_result_submitted":
            recent_activity["submissions"] = int(count or 0)
            recent_activity["last_submitted_at"] = str(timestamp or "")
    diagnostics: list[dict[str, Any]] = []
    if pending_total and auto_enqueue and not worker_enabled:
        diagnostics.append(
            {
                "severity": "broken",
                "code": "auto_enqueue_worker_disabled",
                "message": "Enhancement jobs are being queued while the enhancement worker is disabled.",
                "pending_jobs": pending_total,
            }
        )
    if (
        pending_total
        and worker_enabled
        and worker_mode == "cli_worker"
        and not summary_rows
        and not (recent_activity["claims"] or recent_activity["submissions"])
    ):
        diagnostics.append(
            {
                "severity": "degraded",
                "code": "pending_jobs_no_recent_cli_worker_passes",
                "message": "Pending enhancement jobs exist but no CLI worker pass was recorded in the selected window.",
                "pending_jobs": pending_total,
                "window_hours": hours,
            }
        )
    return {
        "window_hours": hours,
        "configuration": {
            "worker_enabled": worker_enabled,
            "worker_mode": worker_mode,
            "auto_enqueue": auto_enqueue,
            "auto_enqueue_personas": _clean(
                source.get("CHIMERA_MEMORY_ENHANCEMENT_AUTO_ENQUEUE_PERSONAS"),
                max_chars=160,
            ),
            "runtime": runtime,
            "worker_id": worker_id,
            "provider": _clean(
                source.get("CHIMERA_MEMORY_CODEX_WORKER_PROVIDER")
                or source.get("CHIMERA_MEMORY_CLAUDE_WORKER_PROVIDER")
                or source.get("CHIMERA_MEMORY_AGY_WORKER_PROVIDER"),
                max_chars=80,
            ),
        },
        "diagnostics": diagnostics,
        "recent_worker_activity": recent_activity,
        "summary": [
            {
                "runtime": row[0],
                "provider": row[1],
                "worker_id": row[2],
                "passes": int(row[3] or 0),
                "resumed_passes": int(row[4] or 0),
                "jobs_claimed": int(row[5] or 0),
                "jobs_succeeded": int(row[6] or 0),
                "jobs_failed": int(row[7] or 0),
                "tokens_in": int(row[8] or 0),
                "tokens_out": int(row[9] or 0),
                "cache_creation_input_tokens": int(row[10] or 0),
                "cache_read_input_tokens": int(row[11] or 0),
                "avg_latency_ms": int(row[12] or 0),
                "max_latency_ms": int(row[13] or 0),
            }
            for row in summary_rows
        ],
        "pending_jobs": pending_jobs,
        "recent": [
            {
                "created_at": row[0],
                "runtime": row[1],
                "provider": row[2],
                "worker_id": row[3],
                "session_mode": row[4],
                "session_id": row[5],
                "resumed": bool(row[6]),
                "status": row[7],
                "returncode": int(row[8] or 0),
                "jobs_claimed": int(row[9] or 0),
                "jobs_succeeded": int(row[10] or 0),
                "jobs_failed": int(row[11] or 0),
                "tokens_in": int(row[12] or 0),
                "tokens_out": int(row[13] or 0),
                "cache_creation_input_tokens": int(row[14] or 0),
                "cache_read_input_tokens": int(row[15] or 0),
                "latency_ms": int(row[16] or 0),
                "model": row[17],
            }
            for row in recent_rows
        ],
    }


def start_codex_cli_worker_supervisor(
    config: CodexCliWorkerConfig,
    *,
    popen_factory: PopenFactory = subprocess.Popen,
) -> dict[str, object]:
    """Start a background supervisor that launches bounded Codex worker passes."""
    stop_event = threading.Event()
    state: dict[str, object] = {"handle": None, "launch_count": 0, "launch_error_count": 0, "last_error": ""}
    log = logging.getLogger("chimera_memory.cli-worker")

    def _loop() -> None:
        while not stop_event.is_set():
            if not _should_launch_worker_pass(config, log=log):
                state["idle_skip_count"] = int(state.get("idle_skip_count") or 0) + 1
                stop_event.wait(config.restart_interval_seconds)
                continue
            try:
                handle = start_codex_cli_worker_once(config, popen_factory=popen_factory)
            except Exception as exc:
                state["launch_error_count"] = int(state.get("launch_error_count") or 0) + 1
                state["last_error"] = str(exc)
                log.exception("codex cli worker launch failed worker_id=%s", config.worker_id)
                stop_event.wait(config.restart_interval_seconds)
                continue
            state["handle"] = handle
            state["launch_count"] = int(state.get("launch_count") or 0) + 1
            while handle.process.poll() is None and not stop_event.wait(1.0):
                pass
            if stop_event.is_set():
                handle.stop()
                break
            returncode = handle.process.poll()
            _record_cli_worker_pass(config, handle, runtime="codex", returncode=returncode)
            if returncode not in (0, None):
                state["last_error"] = f"process exited {returncode}"
                log.warning(
                    "codex cli worker exited nonzero worker_id=%s returncode=%s stdout=%s stderr=%s",
                    config.worker_id,
                    returncode,
                    handle.stdout_log,
                    handle.stderr_log,
                )
            stop_event.wait(config.restart_interval_seconds)

    thread = threading.Thread(
        target=_loop,
        name="chimera-memory-codex-cli-worker",
        daemon=True,
    )
    thread.start()
    return {"thread": thread, "stop_event": stop_event, "config": config, "state": state}


