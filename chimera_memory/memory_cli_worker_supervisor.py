@dataclass
class CodexCliWorkerHandle:
    process: subprocess.Popen
    stdout_log: Path
    stderr_log: Path
    files: dict[str, str]

    def stop(self, timeout_seconds: float = 5.0) -> None:
        if self.process.poll() is not None:
            return
        self.process.terminate()
        try:
            self.process.wait(timeout=timeout_seconds)
        except subprocess.TimeoutExpired:
            self.process.kill()
            self.process.wait(timeout=timeout_seconds)


def _clean(value: object, *, default: str = "", max_chars: int = 160) -> str:
    text = str(value or "").strip()
    return (text or default)[:max_chars]


def _env_bool(env: Mapping[str, str], key: str, *, default: bool = False) -> bool:
    raw = str(env.get(key, "")).strip().lower()
    if not raw:
        return default
    return raw in {"1", "true", "yes", "on"}


def _env_int(env: Mapping[str, str], key: str, *, default: int, minimum: int, maximum: int) -> int:
    try:
        parsed = int(str(env.get(key, "")).strip())
    except (TypeError, ValueError):
        parsed = default
    return max(minimum, min(maximum, parsed))


def _env_choice(
    env: Mapping[str, str],
    keys: tuple[str, ...],
    *,
    default: str,
    allowed: set[str],
) -> str:
    for key in keys:
        raw = str(env.get(key, "")).strip().lower()
        if raw:
            return raw if raw in allowed else default
    return default


def _state_root(env: Mapping[str, str]) -> Path:
    raw = str(env.get("CHIMERA_MEMORY_STATE_ROOT") or "").strip()
    if raw:
        return Path(raw).expanduser()
    return Path.home() / ".chimera-memory"


def _default_codex_bin(env: Mapping[str, str]) -> str:
    found = shutil.which("codex")
    if found:
        return found
    app_data = str(env.get("APPDATA") or "").strip()
    if app_data:
        candidate = Path(app_data) / "npm" / "codex.cmd"
        if candidate.exists():
            return str(candidate)
    return "codex"


