        worker_id=_clean(source.get("CHIMERA_MEMORY_CODEX_WORKER_ID"), default="codex-memory-worker-1"),
        provider=_clean(source.get("CHIMERA_MEMORY_CODEX_WORKER_PROVIDER"), default="openai", max_chars=80),
