def codex_worker_command(config: CodexCliWorkerConfig) -> list[str]:
    command = [
        config.codex_bin,
        "exec",
        "--json",
        "--ephemeral",
        "--skip-git-repo-check",
        "--cd",
        str(config.worker_root),
    ]
    if config.bypass_approvals_and_sandbox:
        command.append("--dangerously-bypass-approvals-and-sandbox")
